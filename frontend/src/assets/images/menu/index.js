// Add a helper function to get image with fallback
export const getMenuImage = (cuisine, category, item) => {
  try {
    return menuImages[cuisine]?.[category]?.[item] || {
      primary: `https://source.unsplash.com/800x600/?${item.toLowerCase().replace(/\s+/g, '-')}`,
      thumbnail: `https://source.unsplash.com/400x300/?${item.toLowerCase().replace(/\s+/g, '-')}`,
      fallback: `https://source.unsplash.com/800x600/?food`
    };
  } catch (error) {
    console.error('Error getting menu image:', error);
    return {
      primary: `https://source.unsplash.com/800x600/?food`,
      thumbnail: `https://source.unsplash.com/400x300/?food`,
      fallback: `https://source.unsplash.com/800x600/?food`
    };
  }
}; 